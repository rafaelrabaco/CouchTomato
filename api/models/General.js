module.exports = {

  attributes: {

    //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
    //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
    //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
    username: {
      type: 'string',
      required: true,
      unique: true,
      maxLength: 200,
    },

    password: {
      type: 'string',
      required: true,
    },

    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
    //  ╚═╝╩ ╩╚═╝╚═╝═╩╝╚═╝


    //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
    //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
    //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝

  },

  beforeCreate: function (inputs, next) {
    sails.helpers.passwords.hashPassword(inputs.password).exec((err, hashedPassword) => {
      if (err) { return proceed(err); }
      inputs.password = hashedPassword;
      return next();
    });
  },

  beforeUpdate: function (inputs, next) {
    if (inputs.password)
      sails.helpers.passwords.hashPassword(inputs.password).exec((err, hashedPassword) => {
        if (err) { return proceed(err); }
        inputs.password = hashedPassword;
        return next();
      });

    return next();
  }
};

